package springboot.restful.api.Person;

public class Person {
	
	private int PERSON_ID;
	
	private String TITLE;
	
	private String FORE_NAME;
	
	private String SUR_NAME;
	
	private int AGE;
	
	private String GENDER;
	
	private Stirng ADDRESS;

	public int getPERSON_ID() {
		return PERSON_ID;
	}

	public void setPERSON_ID(int pERSON_ID) {
		PERSON_ID = pERSON_ID;
	}

	public String getTITLE() {
		return TITLE;
	}

	public void setTITLE(String tITLE) {
		TITLE = tITLE;
	}

	public String getFORE_NAME() {
		return FORE_NAME;
	}

	public void setFORE_NAME(String fORE_NAME) {
		FORE_NAME = fORE_NAME;
	}

	public String getSUR_NAME() {
		return SUR_NAME;
	}

	public void setSUR_NAME(String sUR_NAME) {
		SUR_NAME = sUR_NAME;
	}

	public int getAGE() {
		return AGE;
	}

	public void setAGE(int aGE) {
		AGE = aGE;
	}

	public String getGENDER() {
		return GENDER;
	}

	public void setGENDER(String gENDER) {
		GENDER = gENDER;
	}

	public Stirng getADDRESS() {
		return ADDRESS;
	}

	public void setADDRESS(Stirng aDDRESS) {
		ADDRESS = aDDRESS;
	}
	

}
